export function getSilhouettesByCategory() {
  const grouped = {};
  Object.entries(SILHOUETTES).forEach(([key, s]) => {
    const cat = s.category || "Other";
    if (!grouped[cat]) grouped[cat] = [];
    grouped[cat].push({ key, ...s });
  });
  return grouped;
}
